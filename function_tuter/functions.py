def area(width, height):
    result = width * height
    return result


result = area(5, 6)
print(result)
result = area(5, 8)
print(result)
result = area(9, 12)
print(result)


def subtract(num1, num2):
    result = num1 - num2
    return result


result = subtract(10, 2)
print(result)
result = subtract(34, 12)
print(result)


def add(num1, num2):
    result = num1 + num2
    return result


result = add(5, 6)
